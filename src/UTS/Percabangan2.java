package UTS;
public class Percabangan2 {
    public static void main(String[] args) {
        int nilai = 85;

        if (nilai >= 90) {
            System.out.println("Nilai Anda: A");
        } 
        else if (nilai >= 80) {
            System.out.println("Nilai Anda: B");
        } 
        else if (nilai >= 70) {
            System.out.println("Nilai Anda: C");
        } 
        else {
            System.out.println("Nilai Anda: D");
        }
    }
}
